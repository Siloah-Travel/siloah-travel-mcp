let currentLocale = "en"

/** Map bare language codes to their default regional variant */
const localeAliases: Record<string, string> = {
  zh: "zh-TW",
  "zh-hans": "zh-CN",
  "zh-hant": "zh-TW",
}

function resolveLocale(locale: string): string {
  // Exact match first
  if (translations[locale]) return locale
  // Check alias map (zh → zh-TW, etc.)
  const alias = localeAliases[locale.toLowerCase()]
  if (alias && translations[alias]) return alias
  // Try language prefix (pt-BR → pt, etc.)
  const lang = locale.split("-")[0]
  if (translations[lang]) return lang
  return "en"
}

export function setLocale(locale: string) {
  currentLocale = resolveLocale(locale)
}

export function t(key: string, params?: Record<string, string | number>): string {
  const dict = translations[currentLocale] ?? translations.en
  let text = dict[key] ?? translations.en[key] ?? key

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v))
    }
    // Handle simple plurals: {s} → "" if count is 1, "s" otherwise
    const count = params.count ?? params.n
    if (count !== undefined) {
      text = text.replace(/\{s\}/g, Number(count) === 1 ? "" : "s")
      text = text.replace(/\{s_en\}/g, Number(count) === 1 ? "" : "en")
      text = text.replace(/\{s_e\}/g, Number(count) === 1 ? "" : "e")
      text = text.replace(/\{s_i\}/g, Number(count) === 1 ? "" : "i")
    }
  }
  return text
}

/** Translate a destination value (e.g. "Mediterranean" → "地中海") */
export function translateDest(dest: string): string {
  const dict = destTranslations[currentLocale] ?? destTranslations[currentLocale.split("-")[0]] ?? destTranslations.en
  return dict[dest] ?? dest
}
